/* 
Variables and Literals

Variables : location in memory

literals : value that can be used directly in code

*/



class VariablesAndLiterals {
    public static void main(String[] args) 
    {

        // Variables creation
        // decleration
        // syntax
        // data_type variable_name;



        // decleration + definition
        // syntax
        // data_type variable_name = value;


        // data-types
        /*
         * 1. int ---- 10,100, 200, 258000
         * 2. float,double  ----- 10.25, 58.6699
         * 3. boolean --- true, false
         * 4. char ---- 'a','b','t',...etc 
         * 5. String ---- "anuraj", "seetha"
         * 
         */


         int age = 18;
         double salary = 45000.5;
         String name = "Anuraj";
         char grade = 'A';
         boolean isIndian = true;

    }
}


